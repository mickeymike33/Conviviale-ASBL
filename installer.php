                    <h3 class="title is-3 separate" id="reinstallation">Réinstallation</h3>
                    <p>
                        La Réinstallation des Réfugiés est une politique internationale menée par le Haut commissariat des Nations-Unies pour les Réfugiés (UNHCR) qui consiste à sélectionner et transférer 
                        des Réfugiés d’un pays où ils ont cherché une protection vers un pays tiers qui a, au préalable, marqué son accord pour leur octroyer un droit de séjour durable. C’est le cas de la Belgique 
                        depuis quelques années.
                    </p>    
                <br>
                    <p>
                        A leur arrivée en Belgique, les Réfugiés réinstallés sont rapidement pris en charge par un des CPAS associés au programme de réinstallation : installation dans le premier logement, service social général, 
                        soutien à l’intégration dans la vie quotidienne. Dans cette dynamique, les CPAS sont notamment épaulés par l’expertise des travailleurs sociaux de Convivial, comme traducteurs et médiateurs interculturels et 
                        accompagnateurs extérieurs. Si besoin, une aide au relogement est apportée à la famille et au CPAS après un an.
                    </p>
