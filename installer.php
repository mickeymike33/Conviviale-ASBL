        <h2>S'installer</h2>
            <h3 class="title is-3 first" id="service-social">Service social</h3>
                <p>
                    Au service social, chaque Réfugié est accueilli par un travailleur qui dresse avec lui un bilan de ses difficultés, 
                    besoins, inquiétudes et qui l’aide à se prendre en charge dans la définition d’un nouveau projet de vie. 
                    Une relation de confiance s’établit et un suivi psychosocial est assuré. Cet accompagnement individuel s’adresse spécifiquement 
                    aux Réfugiés et aux bénéficiaires de la protection subsidiaire récemment reconnus et qui s’installent en Belgique 
                    (les deux premières années).
                </p>
            <ul class="topics">
                <li>
                    <h3 class="title is-5">Aide pour les démarches administratives</h3>
                        <p>
                            Convivial accompagne le Réfugié dans les démarches administratives, notamment avec le CPAS, 
                            la commune, la mutuelle, les services publics, les hôpitaux et l’oriente éventuellement vers d’autres services adaptés. 
                            Dans des situations spécifiques, des accompagnements de terrain sont également proposées pour servir de médiateur et d’interprète. 
                            Afin de veiller à la bonne installation des réfugiés dans leur nouveau logement, des visites à domicile sont organisées.
                        </p>
                </li>
                <li>
                    <h3 class="title is-5">Aide juridique</h3>
                        <p>
                            Convivial informe le Réfugié sur ses droits et devoirs, le soutient dans des situations complexes de médiation (factures, dettes, 
                            relations avec les pouvoirs publics…), assure le suivi des dossiers et si nécessaire l’oriente vers des cabinets spécialisés.
                            Pour les demandeurs d’asile, des informations de base sur la procédure d’asile sont transmises et les personnes sont informées 
                            sur l’accès aux recours en fin de procédure.
                        </p>
                </li>
                <li>
                    <h3 class="title is-5">Aide au regroupement familial</h3>
                        <p>
                            Convivial informe sur les démarches de la procédure de regroupement familial et apporte son soutien dans toutes les étapes de la procédure : 
                            réalisation du dossier, recherche de fonds pour financer les billets d’avion, accueil de la famille regroupée et accompagnement 
                            dans ses démarches d’installation.
                        </p>
                </li>
                    <p class="tag is-warning">ecoute@convivial.be</p>
            </ul>
                <br>
                <h3 class="title is-3 separate" id="logement">Logement</h3>
                    <p>
                        Le service Logement a pour objectif d’aider les réfugiés à trouver un logement durable, décent et à un prix abordable. 
                        Le logement est en effet la première étape d’un réfugié pour commencer sa nouvelle vie en Belgique. C’est également la première difficulté qu’il rencontre 
                        à la sortie du centre d’accueil.
                    </p>
                    <br>
                    <figure class="image">
                        <img src="assets/img/cover-4.jpg">
                    </figure>
                    <p>    
                        Le service Logement propose aux réfugiés un éventail de services qui leur permettent de trouver un logement et de le gérer en « bon père de famille » :
                    </p>
            <ul class="topics">
                <li>
                    <h3 class="title is-5">Recherche d’un logement</h3>
                    <p>
                        Expliquer aux réfugiés comment rechercher un logement, quelles sont les démarches à entreprendre, 
                        et accompagner les plus vulnérables dans leur recherche.
                    </p>
                </li>
                <li>
                    <h3 class="title is-5">Information sur les logements</h3>
                    <p>
                        Informer individuellement et collectivement les réfugiés des droits et obligations du locataire et du propriétaire (signature d’un bail, 
                        paiement d’un loyer et de charges), sur la manière d’habiter et d’entretenir le bien, les économies d’énergie, 
                        la sécurité dans le logement et le tri des déchets.
                    </p>
                </li>
                <li>
                    <h3 class="title is-5">Accompagnement social</h3>
                    <p>
                        Proposer un accompagnement social des réfugiés afin de rassurer les propriétaires : 
                        visite du logement, signature et explication des clauses de contrat, état des lieux et remise des clés.
                        Proposer un accompagnement de terrain afin de faciliter les démarches administratives liées à l’installation.
                    </p>
                </li>
                <li>
                    <h3 class="title is-5">Médiation</h3>
                    <p>
                        En cas de litiges, agir en tant que médiateur entre le réfugié et le propriétaire.
                    </p>
                </li>
                <li>
                    <h3 class="title is-5">Fonds de prêt</h3>
                    <p>
                        Octroi de prêts pour le premier mois de loyer ou une partie de la garantie locative afin de soutenir les réfugiés lorsque toutes 
                        les autres solutions envisagées (CPAS, partenariat avec le CIRE pour les garanties locatives sur compte bloqué) s’avèrent impossibles.
                    </p>
                </li>
                <li>
                    <h3 class="title is-5">Logements de transit</h3>
                    <p>
                        Convivial met à disposition d’isolés et de familles vulnérables en situation d’urgence et en besoin d’un toit des logements de transit 
                        proposés pour une durée d’un an, le temps d’effectuer les démarches administratives d’installation qui constituent un véritable tremplin 
                        pour un futur logement durable et leur intégration en Belgique.
                    </p>
                </li>
                    <br>
                    <p>
                        Convivial recherche activement des propriétaires qui acceptent de louer des logements à des réfugiés pour des baux 
                        classiques.
                    </p>
